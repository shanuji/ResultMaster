                                1 if subject.get("include_in_pass_fail", subject.get("include_pass_fail")) else 0,
                                1 if subject.get("include_in_percentage", subject.get("include_percentage")) else 0,
                                1 if subject.get("include_in_pass_fail", subject.get("include_pass_fail")) else 0,
                                1 if subject.get("include_in_percentage", subject.get("include_percentage")) else 0,
