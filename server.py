def report_context(scope: str = "class", student_id: int | None = None) -> dict:
    payload = workbook_payload()
    rows = calculate_summary(payload)
    if scope == "student" and student_id is not None:
        rows = [row for row in rows if str(next((s["id"] for s in payload["students"] if s["roll_no"] == row["roll_no"]), "")) == str(student_id)]
    return {"settings": payload["settings"], "subjects": payload["subjects"], "rows": rows, "scope": scope}


