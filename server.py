def db() -> sqlite3.Connection:
    con = sqlite3.connect(DB)
    con.row_factory = sqlite3.Row
    con.execute("PRAGMA foreign_keys = ON")
    return con


def sanitize_text(value: object, max_length: int = 160) -> str:
    return str(value or "").strip()[:max_length]


def safe_float(value: object, default: float = 0, minimum: float | None = None, maximum: float | None = None) -> float:
    try:
        number = float(value)
    except (TypeError, ValueError):
        number = default
    if minimum is not None and number < minimum:
        raise ValueError(f"Value must be at least {minimum:g}")
    if maximum is not None and number > maximum:
        raise ValueError(f"Value must be no more than {maximum:g}")
    return number


def validate_sqlite_backup(data: bytes) -> None:
    if not data.startswith(b"SQLite format 3\x00"):
        raise ValueError("Backup must be a valid SQLite database file")
    if len(data) > 50 * 1024 * 1024:
        raise ValueError("Backup file is too large")


